    """Decide artist based on EEG state."""
    if wave == 'Delta':
        print("Young Thug")
    elif wave == 'Theta':
        print("Travis Scott")
    elif wave == 'Alpha':
        print("Future")
    elif wave == 'Beta':
        print("Money Man")
